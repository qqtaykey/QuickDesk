// GetIceConfig handles GET /api/v1/ice-config
// Returns ICE server configuration with time-limited TURN credentials (coturn REST API style).
func (h *APIHandler) GetIceConfig(c *gin.Context) {
	turnURLs := h.settingsService.GetTurnURLs()
	authSecret := h.settingsService.GetTurnAuthSecret()
	ttl := h.settingsService.GetTurnCredentialTTL()
	stunURLs := h.settingsService.GetStunURLs()

	iceServers := []gin.H{}

	if len(turnURLs) > 0 && authSecret != "" {
		expiry := time.Now().Unix() + int64(ttl)
		username := fmt.Sprintf("%d:%s", expiry, generateRandomHex(4))

		mac := hmac.New(sha1.New, []byte(authSecret))
		mac.Write([]byte(username))
		credential := base64.StdEncoding.EncodeToString(mac.Sum(nil))

		iceServers = append(iceServers, gin.H{
			"urls":       turnURLs,
			"username":   username,
			"credential": credential,
		})
	}

	if len(stunURLs) > 0 {
		iceServers = append(iceServers, gin.H{
			"urls": stunURLs,
		})
	}

	c.JSON(http.StatusOK, gin.H{
		"iceServers":       iceServers,
		"lifetimeDuration": fmt.Sprintf("%d.000s", ttl),
	})
}

// GetPreset handles GET /api/v1/preset
// Returns preset configuration as a flat JSON for clients.
func (h *APIHandler) GetPreset(c *gin.Context) {
	preset, err := h.presetService.GetPreset()
	if err != nil {
		log.Printf("Failed to get preset: %v", err)
		c.JSON(http.StatusInternalServerError, gin.H{"error": "failed to get preset"})
		return
	}

	// Build response: parse stored JSON strings into proper JSON objects
	response := gin.H{
		"min_version":   preset.MinVersion,
		"webclient_url": preset.WebclientURL,
	}

	// Parse notice JSON
	if preset.Notice != "" {
		var notice interface{}
		if json.Unmarshal([]byte(preset.Notice), &notice) == nil {
			response["notice"] = notice
		} else {
			response["notice"] = gin.H{}
		}
	} else {
		response["notice"] = gin.H{}
	}

	// Parse links JSON
	if preset.Links != "" {
		var links interface{}
		if json.Unmarshal([]byte(preset.Links), &links) == nil {
			response["links"] = links
		} else {
			response["links"] = gin.H{}
		}
	} else {
		response["links"] = gin.H{}
	}

	c.JSON(http.StatusOK, response)
}

