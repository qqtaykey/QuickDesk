// VerifyPassword handles POST /api/v1/auth/verify
// Request: { "device_id": "123456789", "password": "654321" }
// Response: { "valid": true, "connection_token": "..." } or { "valid": false, "error": "HOST_OFFLINE" }
func (h *APIHandler) VerifyPassword(c *gin.Context) {
	var req struct {
		DeviceID string `json:"device_id" binding:"required"`
		Password string `json:"password" binding:"required"`
	}
	
	if err := c.ShouldBindJSON(&req); err != nil {
		c.JSON(http.StatusBadRequest, gin.H{"error": "invalid request"})
		return
	}
	
	// Check if host is online (has WebSocket connection)
	if h.wsHandler == nil || !h.wsHandler.IsHostOnline(req.DeviceID) {
		log.Printf("Auth verify failed: device %s is offline", req.DeviceID)
		c.JSON(http.StatusOK, gin.H{
			"valid":   false,
			"error":   "HOST_OFFLINE",
			"message": "设备不在线",
		})
		return
	}
	
	// Verify password
	if !h.authService.VerifyDevice(c.Request.Context(), req.DeviceID, req.Password) {
		log.Printf("Auth verify failed: invalid password for device %s", req.DeviceID)
		c.JSON(http.StatusOK, gin.H{
			"valid":   false,
			"error":   "INVALID_PASSWORD",
			"message": "密码错误",
		})
		return
	}
	
	// Generate connection token (simple implementation for now)
	token := generateRandomHex(16)
	
	log.Printf("Auth verify success: device %s", req.DeviceID)
	c.JSON(http.StatusOK, gin.H{
		"valid":            true,
		"connection_token": token,
	})
}

